# ── IEC 81346-2 prefix → Element_Type mapping (§5.12) ──────────────────────
# Each class letter maps to one or more Element_Types.
# For ambiguous prefixes, LLM disambiguation is used (with disk caching).

_IEC_CLASS_TO_TYPES: dict[str, list[str]] = {
    "A": ["Cabinet_Aggregate", "PLC_Module"],
    "B": ["Sensor", "Transducer"],
    "C": ["PLC_Module"],
    "E": ["Heater", "Indicator_Lamp"],
    "F": ["Fuse", "Circuit_Breaker"],
    "G": ["Power_Supply"],
    "H": ["Indicator_Lamp", "Switch"],
    "K": ["Contactor", "Auxiliary_Contactor", "PLC_Module", "Coil"],
    "M": ["Motor", "Actuator", "Valve_Actuator"],
    "P": ["Indicator_Lamp", "Sensor", "Switch"],
    "Q": ["Switch", "Circuit_Breaker", "Contactor", "Main_Contact", "Auxiliary_Contact"],
    "R": ["Sensor", "Transducer"],
    "S": ["Switch"],
    "T": ["Power_Supply"],
    "U": ["PLC_Module", "Power_Supply"],
    "V": ["PLC_Module"],
    "W": ["Power_Supply"],
    "X": ["Terminal", "Terminal_Strip", "Socket_Outlet"],
    "Y": ["Valve_Actuator", "Actuator"],
}

def _classify_by_iec_prefix(
    token: str,
    description: str,
    classifier: "LLMElementClassifier | None" = None,
) -> ClassificationResult | None:
    """Classify by IEC 81346-2 prefix letter extracted from designation.

    For unambiguous prefixes (maps to 1 type), returns directly.
    For ambiguous prefixes (maps to 2+ types), uses LLM for disambiguation
    with context from description and surrounding text.  LLM results are
    cached on disk.
    """
    import re

    # Extract IEC 81346-2 class letter from designation
    # Patterns: "-K1", "-X1:11", "K03", "A14", "-Q2"
    m = re.match(r'^-?([A-Z])\d*', token.strip())
    if not m:
        return None

    prefix_letter = m.group(1).upper()

    # Skip common non-IEC prefixes (O, D, I, J, L, N, Z are not IEC 81346-2 classes)
    if prefix_letter not in _IEC_CLASS_TO_TYPES:
        return None

    candidates = _IEC_CLASS_TO_TYPES[prefix_letter]

    # Unambiguous — return directly (norm-anchored, no LLM needed)
    if len(candidates) == 1:
        etype = candidates[0]
        info = get_aio_element_info(etype.lower().replace(" ", "_"))
        return ClassificationResult(
            element_type=etype,
            iec_81346_2_class=info["iec_class"] if info else prefix_letter,
            caex_type=info["caex_type"] if info else "InternalElement",
            confidence=0.80,
            reasoning=f"IEC 81346-2 class {prefix_letter} -> {etype}",
            source="iec_prefix",
        )

    # Ambiguous — use first candidate as default (norm-anchored, better than Consumer)
    # The downstream LLM classify step can override this with a more specific type
    etype = candidates[0]
    info = get_aio_element_info(etype.lower().replace(" ", "_"))
    return ClassificationResult(
        element_type=etype,
        iec_81346_2_class=info["iec_class"] if info else prefix_letter,
        caex_type=info["caex_type"] if info else "InternalElement",
        confidence=0.55,
        reasoning=f"IEC 81346-2 class {prefix_letter} (ambiguous → {etype} default)",
        source="iec_prefix",
    )


def _keyword_classify(token: str, description: str) -> ClassificationResult | None:
    """Quick classification using LLM when available, minimal fallback otherwise.

    Previously used ~120 hardcoded keyword rules across 20+ categories.
    Now uses LLM for semantic understanding, eliminating language-specific
    keyword lists and adapting to any input language or format.
    """
    return None  # Always fall through to LLM or lookup table — no hardcoded keywords

# Legacy keyword rules removed. The `classify()` method now relies on:
# 1. Disk cache (learned from previous LLM calls)
# 2. Lookup table (aio_schema_mapping.py)
# 3. LLM classification (_llm_classify / _llm_classify_batch)
# No hardcoded keyword matching.


# ── Main classifier ──────────────────────────────────────────────────────────


class LLMElementClassifier:
    """Classify device labels/descriptions into AIO Element_Types.

    Uses LLM as primary classifier with keyword and lookup-table fallbacks.
    Results are cached on disk to avoid repeated LLM calls for the same input.
    """

    # The 29 AIO Element_Types from spec §5
    AIO_ELEMENT_TYPES = [
        "Terminal", "Terminal_Strip",
        "Contactor", "Auxiliary_Contactor",
        "Fuse", "Circuit_Breaker",
        "Switch", "Socket_Outlet",
        "Power_Supply", "PLC_Module",
        "Motor", "Valve_Actuator", "Sensor", "Heater",
        "Transducer", "Actuator", "Consumer",
        "Coil", "Main_Contact", "Auxiliary_Contact",
        "Indicator_Lamp", "Cabinet_Aggregate",
        # Non-spec types (Cable/Resistor/Capacitor/Inductor/Transformer/Rectifier/Connector)
        # are mapped to spec-compliant types via NONSPEC_MAP in classify()
    ]

    def __init__(
        self,
        llm_client: Any | None = None,
        *,
        cache_dir: Path | None = None,
    ) -> None:
        self._client = llm_client
        self._cache = _DiskCache(cache_dir or Path(".iev4pi/cache/element_classifier"))

    def available(self) -> bool:
        try:
            return self._client is not None and self._client.available()
        except Exception:
            return False

    def classify(
        self,
        token: str,
        description: str = "",
        *,
        context_text: str = "",
        force_llm: bool = False,
    ) -> ClassificationResult:
        """Classify a device token/description into an AIO Element_Type.

        Args:
            token: Primary identifier (designation like "-K1:13/14", "HC10N12")
            description: Optional description ("Hilfskontakt Schließer")
            context_text: Optional surrounding text for disambiguation
            force_llm: If True, skip cache and keyword fallback

        Returns:
            ClassificationResult with element_type, iec_81346_2_class, caex_type
        """
        # 1. Check disk cache (validate element_type is spec-compliant)
        if not force_llm:
            cached = self._cache.get(token, description, context_text)
            if cached is not None:
                # Validate cached type — purge if invalid (e.g., from old code)
                if cached.element_type in set(self.AIO_ELEMENT_TYPES):
                    return cached
                # Invalid cached type → fall through to re-classify

        # 2. Try lookup table (exact match by type keyword)
        lookup_key = token.strip().lower()
        # Strip leading hyphen for designation-based lookup
        if lookup_key.startswith("-"):
            lookup_key = lookup_key[1:]
        info = get_aio_element_info(lookup_key)
        if info and not force_llm:
            result = ClassificationResult(
                element_type=info["element_type"],
                iec_81346_2_class=info["iec_class"],
                caex_type=info["caex_type"],
                confidence=0.85,
                reasoning=f"lookup table match: '{token}'",
                source="lookup",
            )
            self._cache.put(token, description, context_text, result)
            return result

        # 3. Try keyword fallback
        if not force_llm:
            kw_result = _keyword_classify(token, description)
            if kw_result is not None:
                # LLM-driven: resolve non-spec types to closest valid spec type
                resolved_type = _resolve_to_valid_type(kw_result.element_type, self)
                kw_result.element_type = resolved_type
                info = get_aio_element_info(resolved_type.lower().replace(" ", "_"))
                if info:
                    kw_result.iec_81346_2_class = info["iec_class"]
                    kw_result.caex_type = info["caex_type"]
                self._cache.put(token, description, context_text, kw_result)
                return kw_result

        # 4. LLM classification
        if self.available():
            llm_result = self._llm_classify(token, description, context_text)
            if llm_result.is_valid:
                self._cache.put(token, description, context_text, llm_result)
                return llm_result

        # 5. Ultimate fallback
        fallback = ClassificationResult(
            element_type="Consumer",
            iec_81346_2_class="M",
            caex_type="InternalElement",
            confidence=0.30,
            reasoning=f"fallback — could not classify '{token}'",
            source="keyword",
        )
        self._cache.put(token, description, context_text, fallback)
        return fallback

    def classify_batch(
        self,
        items: list[tuple[str, str, str]],  # (token, description, context)
    ) -> list[ClassificationResult]:
        """Batch classify multiple items. Uses cache for known items, LLM for new ones."""
        results: list[ClassificationResult | None] = [None] * len(items)

        # First pass: cache + keyword
        uncached: list[int] = []
        for i, (token, desc, ctx) in enumerate(items):
            cached = self._cache.get(token, desc, ctx)
            if cached:
                results[i] = cached
                continue
            lookup_key = token.strip().lower()
            if lookup_key.startswith("-"):
                lookup_key = lookup_key[1:]
            info = get_aio_element_info(lookup_key)
            if info:
                results[i] = ClassificationResult(
                    element_type=info["element_type"],
                    iec_81346_2_class=info["iec_class"],
                    caex_type=info["caex_type"],
                    confidence=0.85, reasoning=f"lookup: '{token}'", source="lookup",
                )
                self._cache.put(token, desc, ctx, results[i])  # type: ignore[arg-type]
                continue
            kw_result = _keyword_classify(token, desc)
            if kw_result:
                info2 = get_aio_element_info(kw_result.element_type.lower().replace(" ", "_"))
                if info2:
                    kw_result.iec_81346_2_class = info2["iec_class"]
                    kw_result.caex_type = info2["caex_type"]
                results[i] = kw_result
                self._cache.put(token, desc, ctx, kw_result)
                continue
            uncached.append(i)

        # Second pass: batch LLM for remaining items
        if uncached and self.available():
            batch_items = [items[i] for i in uncached]
            llm_results = self._llm_classify_batch(batch_items)
            for j, idx in enumerate(uncached):
                if j < len(llm_results) and llm_results[j].is_valid:
                    results[idx] = llm_results[j]
                else:
                    results[idx] = ClassificationResult(
                        element_type="Consumer", iec_81346_2_class="M",
                        caex_type="InternalElement", confidence=0.30,
                        reasoning="batch fallback", source="keyword",
                    )
                token, desc, ctx = items[idx]
                self._cache.put(token, desc, ctx, results[idx])  # type: ignore[arg-type]
        elif uncached:
            for idx in uncached:
                results[idx] = ClassificationResult(
                    element_type="Consumer", iec_81346_2_class="M",
                    caex_type="InternalElement", confidence=0.30,
                    reasoning="no LLM available", source="keyword",
                )

        return [r for r in results if r is not None]  # type: ignore[return-type]

    # ── LLM calls ─────────────────────────────────────────────────────────

    def _llm_classify(
        self, token: str, description: str, context_text: str
    ) -> ClassificationResult:
        system = (
            "You are an expert in IEC 81346 and IEC 60617 industrial electrical engineering "
            "component classification. Classify each device into one of these Element_Types:\n"
            + ", ".join(self.AIO_ELEMENT_TYPES)
            + "\n\nKey rules:\n"
            "- A terminal strip (Klemmenleiste) is Terminal_Strip\n"
            "- A single terminal within a strip is Terminal\n"
            "- A power contactor is Contactor; a small signal relay is Auxiliary_Contactor\n"
            "- Coil, Main_Contact, Auxiliary_Contact are sub-elements of switching devices\n"
            "- A circuit breaker/MCB/RCBO is Circuit_Breaker; a melting fuse is Fuse\n"
            "- A manual switch is Switch; pushbutton/E-stop are also Switch\n"
            "- A PLC I/O module is PLC_Module\n"
            "- An electric motor/pump/stirrer is Motor\n"
            "- A valve actuator is Valve_Actuator\n"
            "- A sensor/transmitter/probe is Sensor\n"
            "- An indicator lamp is Indicator_Lamp\n"
            "- A control cabinet assembly is Cabinet_Aggregate\n"
            "- If the designation follows IEC 81346 prefix: "
            "-X = Terminal/Terminal_Strip, -K = Contactor/Relay, -Q = Switch/Contactor, "
            "-F = Fuse/Circuit_Breaker, -M = Motor, -B = Sensor, -E = Heater, "
            "-T = Power_Supply, -P = Indicator_Lamp, -S = Switch, -A = Cabinet_Aggregate\n"
            "Return JSON: {\"element_type\": \"...\", \"confidence\": 0.0, \"reasoning\": \"...\"}"
        )
        user = f"Classify this device:\n  Designation: {token}\n  Description: {description}"
        if context_text:
            user += f"\n  Context: {context_text[:500]}"

        try:
            raw = self._client.chat_json(system, user)
        except Exception:
            return ClassificationResult()

        if not raw:
            return ClassificationResult()

        etype = str(raw.get("element_type", "")).strip()
        # LLM-driven: resolve any non-spec type to closest valid spec type
        etype = _resolve_to_valid_type(etype, self)
        if etype not in set(self.AIO_ELEMENT_TYPES):
            return ClassificationResult()

        info = get_aio_element_info(etype.lower().replace(" ", "_"))
        return ClassificationResult(
            element_type=etype,
            iec_81346_2_class=info["iec_class"] if info else "",
            caex_type=info["caex_type"] if info else "InternalElement",
            confidence=float(raw.get("confidence", 0.85)),
            reasoning=str(raw.get("reasoning", "")),
            source="llm",
        )

    def _llm_classify_batch(
        self, items: list[tuple[str, str, str]]
    ) -> list[ClassificationResult]:
        """Classify multiple items in one LLM call."""
        item_text = "\n".join(
            f"{i+1}. Designation: \"{token}\", Description: \"{desc}\""
            + (f", Context: \"{ctx[:100]}\"" if ctx else "")
            for i, (token, desc, ctx) in enumerate(items)
        )
        system = (
            "You are an expert in IEC 81346 industrial classification. "
            "Classify each device into its Element_Type. "
            "When Description or Context provides specific information about the device's function "
            "(e.g. lighting, socket, terminal strip, fuse, motor, pump), "
            "PRIORITIZE that over the IEC prefix letter which is less specific. "
            "Available types: " + ", ".join(self.AIO_ELEMENT_TYPES) + "\n"
            "Return JSON: {\"classifications\": [{\"index\": 1, \"element_type\": \"...\", \"confidence\": 0.0}, ...]}"
        )
        user = f"Classify these devices:\n{item_text}"

        try:
            raw = self._client.chat_json(system, user)
        except Exception:
            return []

        classifications = raw.get("classifications", [])
        results: list[ClassificationResult] = []
        for c in classifications:
            etype = str(c.get("element_type", "")).strip()
            etype = _resolve_to_valid_type(etype, self)
            if etype not in set(self.AIO_ELEMENT_TYPES):
                etype = "Consumer"
            info = get_aio_element_info(etype.lower().replace(" ", "_"))
            results.append(ClassificationResult(
                element_type=etype,
                iec_81346_2_class=info["iec_class"] if info else "M",
                caex_type=info["caex_type"] if info else "InternalElement",
                confidence=float(c.get("confidence", 0.85)),
                reasoning=str(c.get("reasoning", "")),
                source="llm",
            ))
        return results
