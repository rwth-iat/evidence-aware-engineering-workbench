"""LLM-based Element_Type classifier for AIO schema compliance.

Replaces the hardcoded ``_component_type()`` (extractor.py:1792) and
``_classify_object()`` (stromlauf_parser.py:693) with LLM-driven classification
into the 29 AIO Element_Types defined in §5 of the schema specification.

Design (per Phase 0.2 validation — 97.3% accuracy):
  - LLM is the primary classifier for ambiguous/se‌mantic cases
  - The lookup table in ``aio_schema_mapping.py`` is the fallback
  - Disk cache (JSON) avoids repeated LLM calls for the same token
  - IDs are NEVER generated by LLM — only classification/content

Usage::

    from iev4pi_transformation_tool.core.llm_element_classifier import (
        LLMElementClassifier, ClassificationResult,
    )
    classifier = LLMElementClassifier(llm_client)
    result = classifier.classify(
        token="-K1:13/14",
        description="Hilfskontakt Schließer",
        context_text="230V Steuerkreis HC10",
    )
    # result.element_type → "Auxiliary_Contact"
    # result.iec_81346_2_class → "Q"
    # result.caex_type → "InternalElement"
"""

from __future__ import annotations

import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

from iev4pi_transformation_tool.core.aio_schema_mapping import (
    ELEMENT_TYPE_MAP,
    get_aio_element_info,
)


@dataclass
class ClassificationResult:
    element_type: str = ""
    iec_81346_2_class: str = ""
    caex_type: str = ""
    confidence: float = 0.0
    reasoning: str = ""
    source: str = ""  # "llm" | "lookup" | "keyword"

    @property
    def is_valid(self) -> bool:
        return bool(self.element_type)


# ── Cache ────────────────────────────────────────────────────────────────────


class _DiskCache:
    """Simple JSON-file cache keyed on (token, description, context_hash)."""

    def __init__(self, cache_dir: Path) -> None:
        self.cache_dir = cache_dir
        self.cache_dir.mkdir(parents=True, exist_ok=True)

    def _key(self, token: str, description: str, context: str) -> str:
        import hashlib
        payload = f"{token.strip().lower()}||{description.strip().lower()}||{context.strip()[:200]}"
        return hashlib.sha256(payload.encode()).hexdigest()[:32]

    def get(self, token: str, description: str, context: str) -> ClassificationResult | None:
        cache_file = self.cache_dir / f"{self._key(token, description, context)}.json"
        if cache_file.is_file():
            try:
                data = json.loads(cache_file.read_text())
                return ClassificationResult(**data)
            except (json.JSONDecodeError, TypeError):
                return None
        return None

    def put(self, token: str, description: str, context: str, result: ClassificationResult) -> None:
        cache_file = self.cache_dir / f"{self._key(token, description, context)}.json"
        cache_file.write_text(json.dumps({
            "element_type": result.element_type,
            "iec_81346_2_class": result.iec_81346_2_class,
            "caex_type": result.caex_type,
            "confidence": result.confidence,
            "reasoning": result.reasoning,
            "source": result.source,
        }, ensure_ascii=False, indent=2))


# ── LLM-driven type resolution — maps non-spec types to closest valid type ────

# In-memory cache for resolved types (avoids repeated LLM calls)
_resolved_type_cache: dict[str, str] = {}

def _resolve_to_valid_type(type_name: str, classifier: Any = None) -> str:
    """Resolve a potentially non-spec Element_Type to the closest valid spec type.

    Uses LLM when available to dynamically determine the best mapping,
    falling back to suffix/prefix matching when LLM is unavailable.
    This eliminates hardcoded NONSPEC_MAP dictionaries.
    """
    et = type_name.strip()
    if not et:
        return "Consumer"

    # Fast path: already a valid type
    if et in set(LLMElementClassifier.AIO_ELEMENT_TYPES):
        return et

    # Check in-memory cache
    cache_key = et.lower().replace(" ", "_")
    if cache_key in _resolved_type_cache:
        return _resolved_type_cache[cache_key]

    # Try LLM resolution
    if classifier is not None and hasattr(classifier, '_client') and classifier._client is not None:
        try:
            if classifier.available():
                valid_types = ", ".join(sorted(LLMElementClassifier.AIO_ELEMENT_TYPES))
                system = (
                    f"You are an electrical engineering classifier. "
                    f"A device was classified as '{et}', which is not in the standard list. "
                    f"Map it to the CLOSEST standard Element_Type from: {valid_types}. "
                    f"Rules: Rectifier/Transformer→Power_Supply, Capacitor/Resistor/Inductor→Consumer, "
                    f"Cable/Connector→Terminal, for unknown types use context clues. "
                    f'Return JSON: {{"resolved_type": "Power_Supply"}}'
                )
                user = f"Map '{et}' to closest standard Element_Type."
                raw = classifier._client.chat_json(system, user)
                resolved = str(raw.get("resolved_type", "")).strip()
                if resolved in set(LLMElementClassifier.AIO_ELEMENT_TYPES):
                    _resolved_type_cache[cache_key] = resolved
                    return resolved
        except Exception:
            pass

    # Minimal fallback (no LLM available): fuzzy match, then Consumer
    el = et.lower().replace(" ", "_")
    for valid in LLMElementClassifier.AIO_ELEMENT_TYPES:
        if valid.lower().replace(" ", "_") == el:
            result = valid
            break
    else:
        result = "Consumer"  # Safe generic fallback — no hardcoded mappings

    _resolved_type_cache[cache_key] = result
    return result


